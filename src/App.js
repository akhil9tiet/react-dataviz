const getPreprocessedData = (dataVal) => {
	//convert t to date format, day, month, year
	return dataVal?.reduce((acc, row) => {
		let date = moment(row.Date);
		let day = date.format("dddd");
		let dayNum = date.format("D");
		let month = date.format("MMM");
		let year = date.format("YYYY");

		let closePriceArr = dataVal?.map((row) => row.Close);
		// let max_closePrice = Math.max.apply(Math, closePriceArr);
		// let min_closePrice = Math.min.apply(Math, closePriceArr);
		let avg_closePrice = closePriceArr.reduce((a, b) => a + b) / closePriceArr.length;
		let stdDev = dev(closePriceArr);
		let normalizedVal = (row.Close - avg_closePrice) / stdDev;

		acc.push({
			dateKey: date._d,
			day: day,
			dayNum: dayNum,
			month: month,
			year: year,
			value: row.Close,
			normalizedVal: normalizedVal,
		});
		return acc;
	}, []);
};

