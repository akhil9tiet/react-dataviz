function dev(arr) {
	let mean =
		arr.reduce((acc, curr) => {
			return acc + curr;
		}, 0) / arr.length;
	arr = arr.map((k) => {
		return (k - mean) ** 2;
	});
	let sum = arr.reduce((acc, curr) => acc + curr, 0);
	let variance = sum / arr.length;

	return Math.sqrt(variance);
}

